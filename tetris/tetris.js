function drawfield() {
    var fieldId = "<table>";
    for (i = 0; i < height; i++) {
        fieldId += "<tr>";
        for (j = 0; j < width; j++) {
            fieldId += "<td id='" + String(i) + " " + String(j) + "'></td>";
        }
        fieldId += "</tr>";
    }
    fieldId += "</table>";
    document.write(fieldId);

}

